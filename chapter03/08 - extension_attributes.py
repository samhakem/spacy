# Extension attributes

# spaCy allows you to set any custom attributes and methods on the Doc,
# Span and Token, which become available as Doc._, Span._ and Token._ –
# for example, Token._.my_attr. This lets you store additional information
# relevant to your application, add new features and functionality to spaCy,
# and implement your own models trained with other machine learning libraries.
# It also lets you take advantage of spaCy’s data structures and the Doc
# object as the “single source of truth”.
